#!/usr/bin/env bash
# Orchestrate the 5-case A/B benchmark.
#
# For each case in cases.json: send the image to raw Claude Opus 4.7
# and to bmg-pro-MCP-with-intent, capture both outputs to
# results/<id>.{raw,bmg}.txt, and emit a side-by-side markdown summary
# to results/summary.md.
#
# Env:
#   ANTHROPIC_API_KEY  (required) — used by raw_opus.py
#   GOOGLE_API_KEY     (required) — used by `bmg describe`; also
#                                   resolves from ~/.config/bmg/api_key.

set -euo pipefail

HERE="$(cd "$(dirname "$0")" && pwd)"
RESULTS="$HERE/results"
PY="$HERE/.venv/bin/python"
BMG="${BMG:-bmg}"

mkdir -p "$RESULTS"

if [[ -z "${ANTHROPIC_API_KEY:-}" ]]; then
  echo "ANTHROPIC_API_KEY not set — set it before running the raw-Opus condition" >&2
  exit 1
fi

CASES_JSON="$HERE/cases.json"
N=$($PY -c "import json; print(len(json.load(open('$CASES_JSON'))['cases']))")

echo "Running $N cases through raw-Opus and bmg-pro..."

# Emit per-case captures.
for i in $(seq 0 $((N - 1))); do
  ID=$($PY -c "import json; print(json.load(open('$CASES_JSON'))['cases'][$i]['id'])")
  IMG=$($PY -c "import json,os; c=json.load(open('$CASES_JSON'))['cases'][$i]; print(os.path.join('$HERE', c['image']))")
  Q=$($PY -c "import json; print(json.load(open('$CASES_JSON'))['cases'][$i]['question'])")
  INTENT=$($PY -c "import json; print(json.load(open('$CASES_JSON'))['cases'][$i]['intent'])")
  PROFILE=$($PY -c "import json; print(json.load(open('$CASES_JSON'))['cases'][$i].get('profile','general'))")

  echo
  echo "=== $ID (profile=$PROFILE) ==="
  echo "  image:  $IMG"
  echo "  q:      $Q"

  echo "  --> raw Opus..."
  if $PY "$HERE/raw_opus.py" "$IMG" "$Q" > "$RESULTS/$ID.raw.txt" 2> "$RESULTS/$ID.raw.err"; then
    echo "     ok ($(wc -c < "$RESULTS/$ID.raw.txt") bytes)"
  else
    echo "     FAILED — see $RESULTS/$ID.raw.err"
  fi

  echo "  --> bmg (profile=$PROFILE)..."
  if "$BMG" describe -model gemini-2.5-pro -profile "$PROFILE" -intent "$INTENT" -pretty=true "$IMG" \
        > "$RESULTS/$ID.bmg.json" 2> "$RESULTS/$ID.bmg.err"; then
    # Extract the summary line for the prose half of the side-by-side.
    $PY -c "import json; d=json.load(open('$RESULTS/$ID.bmg.json')); print(d['summary'])" \
        > "$RESULTS/$ID.bmg.txt"
    echo "     ok ($(wc -c < "$RESULTS/$ID.bmg.json") bytes JSON)"
  else
    echo "     FAILED — see $RESULTS/$ID.bmg.err"
  fi
done

# Compose side-by-side markdown summary.
$PY - <<'PYEOF' > "$RESULTS/summary.md"
import json
import pathlib

HERE = pathlib.Path("benchmark")
RESULTS = HERE / "results"
cases = json.load(open(HERE / "cases.json"))["cases"]

def maybe_read_text(p):
    try:
        return p.read_text().strip()
    except FileNotFoundError:
        return "_(no output captured — see .err)_"

def maybe_read_json(p):
    try:
        return json.load(open(p))
    except FileNotFoundError:
        return None

# Each profile has its own structured payload shape. Picks the
# subset of fields most relevant for the README reader to skim.
PROFILE_KEYS = {
    "general":    ["elements"],
    "chart":      ["chart_type", "title", "axes", "series", "key_observations"],
    "diagram":    ["diagram_type", "nodes", "edges"],
    "document":   ["doc_type", "headings", "paragraphs", "tables", "key_facts"],
    "screenshot": ["screen_title", "elements", "focused_element_label"],
    "code":       ["language", "context", "code_text", "annotations", "has_error"],
}

def structured_excerpt(profile: str, structured: dict | None) -> str:
    if not structured:
        return ""
    keys = PROFILE_KEYS.get(profile, PROFILE_KEYS["general"])
    excerpt = {k: structured.get(k) for k in keys if k in structured}
    return json.dumps(excerpt, indent=2, ensure_ascii=False)

print("# bmg vs raw Opus 4.7 — side-by-side\n")
print(f"_Auto-generated by `benchmark/run.sh` over {len(cases)} synthetic cases. "
      "Each row pairs the same (image, question) sent to (a) raw Claude Opus 4.7 "
      "via the Anthropic API and (b) `bmg describe -model gemini-2.5-pro -profile <P> "
      "-intent <I>` (the deliberate MCP surface bmg exposes via "
      "`mcp__bemygeminis__bmg_describe`). bmg's reply has two parts shown: the prose "
      "summary, then the profile-typed structured JSON output._\n")

for c in cases:
    profile = c.get("profile", "general")
    print(f"## {c['id']} — {c['category']} _(profile=`{profile}`)_\n")
    print(f"**Image:** `{c['image']}`  ")
    print(f"**Question:** {c['question']}  ")
    print(f"**What it tests:** {c['what_it_tests']}\n")

    raw = maybe_read_text(RESULTS / f"{c['id']}.raw.txt")
    bmg_summary = maybe_read_text(RESULTS / f"{c['id']}.bmg.txt")
    bmg_full = maybe_read_json(RESULTS / f"{c['id']}.bmg.json")
    structured = bmg_full.get("structured") if bmg_full else None

    print("**raw Opus 4.7 (prose):**")
    print()
    print("> " + raw.replace("\n", "\n> "))
    print()

    print(f"**bmg → Gemini-2.5-Pro (profile=`{profile}`):**")
    print()
    print("> " + bmg_summary.replace("\n", "\n> "))
    print()

    excerpt = structured_excerpt(profile, structured)
    if excerpt:
        print("Structured output (excerpt):")
        print()
        print("```json")
        print(excerpt)
        print("```")
        print()
PYEOF

echo
echo "Done. Summary: $RESULTS/summary.md"
